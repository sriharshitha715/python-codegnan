#And operator
a = 2
b = 1
print(a &b)

# OR operator
c = 3
d = 5
print(c or d)

# Left shift
print(10<<2)

#Right shift
print(21>>2)

# Member ship operators
print(2 in [1,3,4])
print(2 not in [1,3,4])

# Identity operators

print(2 is [1,3,4])
print(2 is not [1,3,4])

